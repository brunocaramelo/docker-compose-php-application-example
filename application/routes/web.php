Route::get('coverage-report', function ()
{
    $path = storage_path('data_import/_reports.zip');
    
    $file = File::get($path);
    $type = File::mimeType($path);

    $response = Response::make($file, 200);
    $response->header("Content-Type", $type);

    return $response;
});